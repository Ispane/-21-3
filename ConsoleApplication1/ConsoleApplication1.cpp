#include <iostream> 
using namespace std;

double A(double n, int m)
{
    double s = 0, a = 0;
    s += n;
    a = s / m;
    return a;
}
int main()
{
    double n, a = 0;
    int m;
    cout << "Сколько цифр будет введенно - ";
    cin >> m;
    cout << " " << endl;
    for (int i = 0; i < m; i++)
    {
        cout << " Цифры: ";
        cin >> n;
        a += A(n, m);
    }
    cout << "Сред. ариф. = " << a << endl;
    return 0;
}


